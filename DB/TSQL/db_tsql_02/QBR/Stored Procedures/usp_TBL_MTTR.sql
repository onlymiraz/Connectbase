CREATE PROCEDURE QBR.[usp_TBL_MTTR]

AS
	SET XACT_ABORT, NOCOUNT ON;
BEGIN TRY

	DECLARE @EVENTID INT;

	INSERT INTO [LOG].tbl_StoreProc
    (
		[EVENTNAME],
		[EVENTSTART],
		[EVENTTYPE],
		[EVENTDESCRIPTION]
	)
	VALUES
	(
		'QBR.usp_TBL_MTTR',
		CAST(GETDATE() AS DATETIME),
		'STORE PROC',
		'SINGLE_INGESTION'
	)

	SET @EVENTID = SCOPE_IDENTITY();

	BEGIN TRANSACTION

	DELETE FROM wad_prd_02.[QBR].[TBL_MTTR] WHERE YEAR(CLOSED_DT)>=2024

	INSERT INTO QBR.TBL_MTTR
	SELECT A.*, CAST(GETDATE() AS DATE) AS UPDATE_DT FROM LZ.TBL_MTTR A
	
	COMMIT TRANSACTION

	UPDATE L
    SET L.EVENTEND = CAST(GETDATE() AS DATETIME)
    FROM LOG.tbl_StoreProc L
    WHERE L.EVENTID = @EventID;

END TRY
BEGIN CATCH
	IF @@trancount > 0 ROLLBACK TRANSACTION
	EXEC usp_error_handler
	RETURN 55555
END CATCH