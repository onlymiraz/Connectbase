
--ILEC LINES  
SELECT DISTINCT B.SUB_METRICS_NO, A.DCY_ID, C.LOCATION_CODE, A.DTE_ID, A.DENOMINATOR LINES
FROM CAMP.FACT_SUB_METRICS A, CAMP.DIM_METRICS B, CAMP.DIM_GEOGRAPHY C
WHERE A.DTE_ID IN ('20200401') --CHANGE EACH MONTH  YYYYMMDD 
AND A.DMS_ID = B.ID
AND A.DGY_ID = C.ID 
AND C.LOCATION_CODE IN ('OR','WA')
AND SUB_METRICS_NO IN ( 
'MR-2-01-3221','MR-2-01-3223','MR-2-01-3561','MR-2-01-3562',
'MR-2-01-3563','MR-2-01-3605','MR-2-01-3606')
AND DCY_ID = '100'
AND FLAG = 'JPSAC2C'   
AND DENOMINATOR > 0
ORDER BY 3,2,1;


