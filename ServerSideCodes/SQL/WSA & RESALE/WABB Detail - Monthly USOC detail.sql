SELECT CASE WHEN CARRIER_NAME IS NOT NULL THEN CARRIER_NAME ELSE CARRIER END CARRIER, SERVICE_TYPE,
       CCNA, STATE, TARIFF_CD, BAN_TYPE, WTN, BTN, MASTERACCOUNTNUMBER, CUSTNAME, ADDRESS, CITY, ZIPCODE, SITE_CLLI,
