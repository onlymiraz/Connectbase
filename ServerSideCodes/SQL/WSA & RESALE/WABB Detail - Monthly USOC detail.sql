       SUBSTR(TRIM(BILLINGTELEPHONE),1,10) BTN, 
       SUBSTR(TRIM(C2F.MASTERACCOUNTNUMBER),1,10) MASTERACCOUNTNUMBER, 
       C2F.ADDRESS,
       C2F.CITY,
       C2F.STATE,
       C2F.ZIPCODE,
